class WPBakeryShortCode_statement extends WPBakeryShortCodesContainer {
    static function statement_func( $atts,  $content ) {
        extract( shortcode_atts( array( 
            'parallax' => 'false',
            'parallax_overlay' => 'false',
            'image' => '',
            'color' => '',
