$google_maps_counter = 0;
class WPBakeryShortCode_google_maps extends WPBakeryShortCodesContainer {
    static function anps_google_maps_func( $atts,  $content ) {        
        global $google_maps_counter;
        $google_maps_counter++;
        extract( shortcode_atts( array(
                    'zoom'   => '15'
        ), $atts ) ); 
        preg_match_all( '#\](.*?)\[/google_maps_item]#', $content, $matches); 
        $location = $matches[1][0]; 
        wp_enqueue_script('gmap3_link');
        wp_enqueue_script('gmap3');
        return "<script>
            jQuery(document).ready(function( $ ) { 
                    $('#map$google_maps_counter').gmap3({
                        map:{
                            options:{  
                                zoom: $zoom   
                            }
                        },
                        marker:{
                          values:[
                            ".do_shortcode($content)."
                          ],
                          options:{
                            draggable: false
                          },
                          events: {
                            mouseover: function(marker, event, context){
                                var map = $(this).gmap3('get'),
                                infowindow = $(this).gmap3({get:{name:'infowindow'}});
                                if (infowindow){
                                  infowindow.open(map, marker);
                                  infowindow.setContent(context.data);
                                } else {
                                  $(this).gmap3({
                                    infowindow:{
                                      anchor:marker,
                                      options:{content: context.data}
                                    }
                                  });
                                }
                              },
                            mouseout: function(){
                                var infowindow = $(this).gmap3({get:{name:'infowindow'}});
                                if (infowindow){
                                  infowindow.close();
                                }
                              }  
                          }
                        }, 
                        getlatlng:{
                            address: '".$location."',
                            callback: function(results){ 
                                if(!results) return;
                                $(this).gmap3({
                                    map:{
                                        options: {
                                            center: [results[0].geometry.location.lat(), results[0].geometry.location.lng()]
                                        }
                                    }
                                });
                            }
                        }
                    });   
                });
            </script>
            <div class='map' id='map$google_maps_counter'></div>";
    }
}
add_shortcode('google_maps', array('WPBakeryShortCode_google_maps','anps_google_maps_func'));
